function createMemoryFiles(targetRoot, dryRun = false) {
  const memoryRoot = path.join(targetRoot, ".github", "agent_memory");
  ensureDir(memoryRoot);

  // Create main memory files
  for (const [filename, content] of Object.entries(memoryTemplates)) {
    const filePath = path.join(memoryRoot, filename);
    if (!dryRun) {
      fs.writeFileSync(filePath, content);
    }
  }

  // Create phases subdirectory with placeholder phase READMEs
  const phasesRoot = path.join(memoryRoot, "phases");
  const phases = [
    "Phase 1 - Foundation",
    "Phase 2 - Implementation",
    "Phase 3 - Testing",
    "Phase 4 - Deployment"
  ];

  for (const phase of phases) {
    const phaseDir = path.join(phasesRoot, phase);
    if (!dryRun) {
      ensureDir(phaseDir);
      fs.writeFileSync(path.join(phaseDir, "README.md"), `# ${phase}\n\n_Placeholder - to be configured by Agentic Brain Installer_\n`);
    }
  }

  return memoryRoot;
}

